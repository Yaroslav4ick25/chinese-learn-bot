def clean_translation(word):
    rus = word.get("translations", {}).get("rus") or []
    for t in rus:
        t = t.strip()
        if t and not t.startswith("("):
            return t[:60]
    if rus:
        return rus[0].strip()[:60]
    eng = word.get("translations", {}).get("eng") or []
    return (eng[0].strip()[:60] if eng else "?")


def clean_pinyin(word):
    return word.get("pinyin", "").strip()


def build_quiz(words):
    quiz = []
    pool = list(range(len(words)))
    random.shuffle(pool)
    for qi in pool[:4]:
        correct_word = words[qi]
        distractors = [w for i, w in enumerate(words) if i != qi]
        random.shuffle(distractors)
        distractors = distractors[:3]
        if len(distractors) < 3:
            continue
        options_words = distractors + [correct_word]
        random.shuffle(options_words)
        letters = ["A", "B", "C", "D"]
        options = {letters[i]: w["translation"] for i, w in enumerate(options_words)}
        answer = letters[options_words.index(correct_word)]
        quiz.append({
            "text": f"Что означает <b>{correct_word['hanzi']}</b> ({correct_word['pinyin']})?",
            "options": options,
            "answer": answer,
        })
    return quiz


def main():
    with open(os.path.join(BASE_DIR, "data", "hsk.json"), encoding="utf-8") as f:
        raw = json.load(f)

    raw.sort(key=lambda w: w["id"])

    by_level = {}
    for w in raw:
        by_level.setdefault(w["level"], []).append(w)

    lessons = []
    lesson_id = 1
    dialogue_idx = 0
    for level in sorted(by_level):
        words_in_level = by_level[level]
        grammar_pool = GRAMMAR_POOL[level]
        grammar_idx = 0
        chunk_num = 0
        for start in range(0, len(words_in_level), WORDS_PER_LESSON):
            chunk = words_in_level[start:start + WORDS_PER_LESSON]
            chunk_num += 1
            words = [{
                "hanzi": w["hanzi"],
                "pinyin": clean_pinyin(w),
                "translation": clean_translation(w),
            } for w in chunk]

            quiz = build_quiz(words)
            if not quiz:
                quiz = [{
                    "text": f"Что означает <b>{words[0]['hanzi']}</b> ({words[0]['pinyin']})?",
                    "options": {"A": words[0]["translation"], "B": "неверно", "C": "неверно", "D": "неверно"},
                    "answer": "A",
                }]

            lessons.append({
                "id": lesson_id,
                "level": LEVEL_NAMES[level],
                "title": f"{LEVEL_NAMES[level]}, урок {chunk_num}",
                "words": words,
                "grammar": grammar_pool[grammar_idx % len(grammar_pool)],
                "dialogue": dialogue_idx % len(DIALOGUES),
                "quiz": quiz,
            })
            lesson_id += 1
            grammar_idx += 1
            dialogue_idx += 1

    out_path = os.path.join(BASE_DIR, "lessons.py")
    with open(out_path, "w", encoding="utf-8") as f:
        f.write('# -*- coding: utf-8 -*-\n')
        f.write('"""Автосгенерировано build_lessons.py из data/hsk.json (5000 слов HSK 1-6).\n')
        f.write('Не редактировать вручную - запустите build_lessons.py заново."""\n\n')
        f.write("DIALOGUES = ")
        f.write(repr(DIALOGUES))
        f.write("\n\n")
        f.write("LESSONS = ")
        f.write(repr(lessons))
        f.write("\n")

    print(f"Generated {len(lessons)} lessons across {len(by_level)} HSK levels.")
    for level in sorted(by_level):
        n = sum(1 for l in lessons if l["level"] == LEVEL_NAMES[level])
        print(f"  {LEVEL_NAMES[level]}: {n} lessons, {len(by_level[level])} words")


if __name__ == "__main__":
    main()
